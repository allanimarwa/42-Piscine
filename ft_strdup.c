#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*s2;
	int		i;

	i = 0;
	s2 = malloc(ft_strlen(s) + 1);
	if (s2 == NULL)
		return (s2);
	while (s [i] != '\0')
	{
		s2[i] = s [i];
		i++;
	}
	s2 [i +1] = '\0';
	return (s2);
}
